public class ComparisonOperators {
    public static void main(String[] args) {
        int firstScore = 3;
        int secondScore = 8;

        if (firstScore == secondScore)
            System.out.println("First score is equal to second score");
        else {
            System.out.println("The scores aren't equal");
        }

        // the other equality operators are >, >=, <=, !=, <
    }
}